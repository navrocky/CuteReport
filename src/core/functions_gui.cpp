#include "functions_gui.h"

#include <QtCore>
#include <QWidget>


namespace CuteReport
{


QString widgetGeometryToString(QWidget * widget)
{
    return QString::number(widget->width()) + "," + QString::number(widget->height());
}


bool widgetGeometryFromString(QWidget * widget, const QString & geometry)
{
    bool ok;

    int width = geometry.section(",", 0, 0).toInt(&ok);
    if (!ok) return false;

    int height = geometry.section(",", 1, 1).toInt();
    if (!ok) return false;

    widget->resize(width, height);

    return true;
}

}// namespace
